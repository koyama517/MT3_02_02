Matrix4x4 MakeViewportMatrix(float left, float top, float widte, float height, float minDepth, float maxDepth) {
	Matrix4x4 result;

	result.m[0][0] = widte / 2;
	result.m[0][1] = 0;
	result.m[0][2] = 0;
	result.m[0][3] = 0;

	result.m[1][0] = 0;
	result.m[1][1] = -height / 2;
	result.m[1][2] = 0;
	result.m[1][3] = 0;

	result.m[2][0] = 0;
	result.m[2][1] = 0;
	result.m[2][2] = maxDepth - minDepth;
	result.m[2][3] = 0;

	result.m[3][0] = left + widte / 2;
	result.m[3][1] = top + height / 2;
	result.m[3][2] = minDepth;
	result.m[3][3] = 1;

	return result;
}

Matrix4x4 MakePerspectiveFovMatrix(float fovY, float aspectRatio, float nearClip, float farClip) {
	Matrix4x4 result;

	result.m[0][0] = (1 / aspectRatio) * (1 / std::tan(fovY / 2));
	result.m[0][1] = 0;
	result.m[0][2] = 0;
	result.m[0][3] = 0;

	result.m[1][0] = 0;
	result.m[1][1] = 1 / std::tan(fovY / 2);
	result.m[1][2] = 0;
	result.m[1][3] = 0;

	result.m[2][0] = 0;
	result.m[2][1] = 0;
	result.m[2][2] = farClip / (farClip - nearClip);
	result.m[2][3] = 1;

	result.m[3][0] = 0;
	result.m[3][1] = 0;
	result.m[3][2] = -(nearClip * farClip) / (farClip - nearClip);
	result.m[3][3] = 0;

	return result;
}

float Dot(const Vector3& v1, const Vector3& v2) {
	float result;

	return result = v1.x * v2.x + v1.y * v1.y + v1.z * v2.z;

};

float Length(const Vector3& v) {

	return sqrt(Dot(v, v));
};

Vector3 Project(const Vector3& v1, const Vector3& v2) {
	Vector3 result;

	result = Multiply(Multiply(v1, v2), v2);

	return result;
}

Vector3 ClosestPoint(const Vector3& point, const Segment& segment) {
	Vector3 result{};
	float t{};

	// 計算処理
	t = Dot(Subtract(point, segment.origin), segment.diff) / std::powf(Length(segment.diff), 2.0f);
	result = Add(segment.origin, F2VMultiply(t, segment.diff));
	//t = Clamp(t, 1.0f, 0.0f);

	return result;
}

bool InCollision(const Sphere& s1, const Sphere& s2) {
	bool result;
	float distance = Length(Subtract(s2.center,s1.center));

	if (distance <= s1.radius + s2.radius)
	{
		result = true;
	}
	else
	{
		result = false;
	}

	return result;
}

bool InCollision(const Sphere& s, const Plane& p) {
	bool result;

	float k{};

	k = Dot(p.normal, s.center) - p.distance;

	k = std::abs(k);

	if (k < s.radius)
	{
		result = true;
	}
	else
	{
		result = false;
	}

	return result;
}

Vector3 Prependicular(const Vector3& vector) {

	if (vector.x != 0.0f || vector.y != 0.0f) {
		return { -vector.y, vector.x, 0.0f };
	}
	return { 0.0f, -vector.z, vector.y };
};

Vector3 Normalize(const Vector3& v) {

	float length = Length(v);
	Vector3 result;
	if (!length == 0)
	{
		result.x = v.x / length;
		result.y = v.y / length;
		result.z = v.z / length;
	}

	return result;

};
Vector3 Cross(const Vector3& v1, const Vector3& v2) {
	Vector3 result;

	result = { v1.y * v2.z - v1.z * v2.y,
		v1.z * v2.x - v1.x * v2.z,
		v1.x * v2.y - v1.y * v2.x };

	return result;
}